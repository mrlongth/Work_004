        private bool saveData()
        {
            bool blnResult = false;
            bool blnDup = false;
            string strMessage = string.Empty;
            string strwork_code = string.Empty,
                strwork_year = string.Empty,
                strwork_name = string.Empty,
                strActive = string.Empty,
                strCreatedBy = string.Empty,
                strUpdatedBy = string.Empty;
            string strScript = string.Empty;
            cWork oWork = new cWork();
            DataSet ds = new DataSet();
            try
            {
                #region set Data
                strwork_code = txtwork_code.Text.Trim();
                strwork_name = txtwork_name.Text;
                strwork_year = cboYear.SelectedItem.Value;
                if (chkStatus.Checked == true)
                {
                    strActive = "Y";
                }
                else 
                {
                    strActive = "N";
                }
                strCreatedBy = Session["username"].ToString();
                strUpdatedBy = Session["username"].ToString();
                #endregion
                if (ViewState["mode"].ToString().ToLower().Equals("edit"))
                {
                    #region edit
                    if (!blnDup)
                    {
                        if (oWork.SP_UPD_WORK(strwork_code, strwork_year,strwork_name, strActive, strUpdatedBy, this.BudgetType, ref strMessage))
                        {
                            blnResult = true;
                        }
                        else
                        {
                            lblError.Text = strMessage.ToString();
                        }
                    }
                    else
                    {
                        ScriptManager.RegisterStartupScript(Page, Page.GetType(), "frMainPage", strScript, true);
                    }
                    #endregion
                }
                else
                {
                    #region check dup
                    string strCheckDup = string.Empty;
                    strCheckDup = " and work_name = '" + strwork_name.Trim() + "' and work_year = '" + strwork_year  + "' ";
                    if (!oWork.SP_SEL_WORK(strCheckDup, ref ds, ref strMessage))
                    {
                        lblError.Text = strMessage;
                    }
                    else
                    {
                        if (ds.Tables[0].Rows.Count > 0)
                        {
                            strScript = 
                                "alert(\"ไม่สามารถเพิ่มข้อมูล เนื่องจากข้อมูล " + strwork_name.Trim() + " ปี " + strwork_year.Trim() + "  ซ้ำ\");\n" ;
                            blnDup = true;
                        }
                    }
                    #endregion
                    #region insert
                    if (!blnDup)
                    {
                        if (oWork.SP_INS_WORK(strwork_year, strwork_name, strActive, strCreatedBy, this.BudgetType, ref strMessage))
                        {
                            string  strGetcode = " and work_name = '" + strwork_name.Trim() + "' and work_year = '" + strwork_year + "' ";
                            if (!oWork.SP_SEL_WORK(strGetcode, ref ds, ref strMessage))
                            {
                                lblError.Text = strMessage;
                            }
                            if (ds.Tables[0].Rows.Count > 0)
                            {
                                strwork_code = ds.Tables[0].Rows[0]["work_code"].ToString();
                            }
                            ViewState["work_code"] = strwork_code;
                            blnResult = true;
                        }
                        else
                        {
                            lblError.Text = strMessage.ToString();
                        }
                    }
                    else
                    {
                        ScriptManager.RegisterStartupScript(Page, Page.GetType(), "frMainPage", strScript, true);
                    }
                    #endregion
                }
            }
            catch (Exception ex)
            {
                lblError.Text = ex.Message.ToString();
            }
            finally
            {
                oWork.Dispose();
            }
            return blnResult;
        }

